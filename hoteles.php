      	<h2 class="desti">Ubicación</h2>
      		<p class="texto">Debes tener presente qué planes vas a hacer y dónde está ubicado el hotel, generalmente los hoteles más retirados de las zonas turísticas son más baratos, pero haz cuentas de cuánto tendrás que invertir en transporte para llegar todos los días a las zonas que irás a visitar, además del tiempo que te tomaría.</p>
     	 <h2 class="desti">Transporte</h2>
      		<p class="texto">Revisa que el hotel donde te quieres hospedar tenga fácil acceso al transporte público, especialmente en ciudades grandes donde los trayectos son muy largos y debes sí o sí tomar el tren o taxi. En ocasiones por buscar un hotel económico resultamos hospedándonos en lugares muy lejos y terminamos invirtiendo más en transporte.</p>
		<h2 class="desti">Reserva con anticipación</h2>
            <p class="texto">Reserva con 3 o 5 meses de anticipación, en lugares como Europa, Asia, Medio Oriente y Suramérica los precios se disparan en temporada, la reserva con tiempo te ayudará a ahorrar hasta un 20%.</p>
		<h2 class="desti">Habitación</h2>
			<p class="texto">Como todo depende de los gustos y los planes que tengas; si tienes pensado salir temprano y regresar en la noche, pues probablemente solo necesites una buena cama para dormir. Si por el contrario piensas pasar más tiempo en tu habitación, te recomendamos que busques una con más lujos; puede ser una habitación más grande, que tenga televisión satelital, con balcón, una hermosa vista, etc.</p>
		
