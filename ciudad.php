  <h2 class="desti">La Candelaria</h2>
        
        <div class="centrar">
          <img class="sty" style="padding: 0px;" src="imagenes\candelaria.jpg" alt="cadelaria" width="300" height="300"/>
        </div>
          <p class="texti">El sector institucional de La Candelaria, situado en la parte baja de la zona, se caracteriza por agrupar las edificaciones que son sede de los diferentes órganos del gobierno nacional y del Distrito Capital, pues en esta se encuentra la sede de la Presidencia de la República y residencia presidencial, conocida como la Casa de Nariño, la sede del Congreso de la República, el Capitolio Nacional, la Corte Suprema de Justicia de Colombia y el Palacio Liévano, que alberga la Alcaldía Mayor de Bogotá.</p>
          <p class="text">En el centro de las últimas tres edificaciones se encuentra la Plaza de Bolívar. En el marco de esta plaza también se ubican edificaciones religiosas de gran interés, como la Catedral Basílica Metropolitana de Bogotá y Primada de Colombia y la Capilla del Sagrario. La Casa Museo del 20 de julio se halla en el costado nororiental del lugar, y en el noroccidental, detrás del Palacio Arzobispal, la la iglesia de la Concepción en la calle Décima con carrera Novena y la iglesia San Juan de Dios en la calle Doce con carrera Décima.</p>
          <p class="texti">Se encuentran otros importantes museos como el del siglo xix, el de Artes y Tradiciones Populares, la Casa Museo Francisco José de Caldas y la Iglesia Museo de Santa Clara, todos ellos ubicados sobre la carrera Octava entre calles Séptima y Novena. El sector alberga centros e instituciones culturales como los teatros de La Candelaria, el Libre y el Colón, lo mismo que las salas de conciertos del Camarín del Carmen, de la Fundación Gilberto Alzate Avendaño y de la Biblioteca Luis Ángel Arango. Otros sitios de interés cultural son la Imprenta Nacional, el Observatorio Astronómico Nacional, la Quinta de Bolívar, el Archivo General de la Nación, la Casa de Poesía Silva y la Plaza del Chorro de Quevedo.</p>
  <h2 class="title">Parques y Plazas</h2>

    <h2 class="desti">Parque Simón Bolivar</h2>
        <div class="centrar">
           <img class="sty" style="padding: 0px;" src="imagenes\parque.jpg" alt="parque" width="300" height="300"/>
         </div>
        <p class="text">El Parque Metropolitano Simón Bolívar es un conjunto de parques, zonas verdes y un excelente centro de desarrollo de la ciudad que se encuentra en el centro geográfico de la misma, cerca de la zona El Salitre y de la Universidad Nacional de Colombia Se empezó a construir en 1966 y durante algún tiempo solo albergó una caseta deportiva. En 1968 en ocasión a la visita del Papa Pablo VI para el 39º Congreso Eucarístico Internacional se construyó un pequeño templo, al cual se le denominó Templete Eucarístico. Para esa época el parque ya contaba con lagos y se sembraban los primeros árboles. Posteriormente se construyó un segundo templete para la visita del Papa Juan Pablo II en junio de 1986.</p>
        <p class="texti">Actualmente el parque cuenta con una infraestructura de ciclovías, caminos peatonales, parqueo para automotores y una plaza ceremonial (conocida como la Plaza de Eventos) con capacidad para 140.000 personas; la mayor parte de esta infraestructura fue remodelada, restaurada o readecuada además de las adiciones de ciclorutas, alamedas y otras obras importantes.17​ En la actualidad este parque es el escenario de muchos eventos culturales y recreacionales como conciertos, ferias, festivales y competencias.</p>
  <h2 class="desti">Parque Mundo Aventura</h2>
      <div class="centrar">
         <img class="sty" style="padding: 0px;" src="imagenes\mundo.jpg" alt="mundo" width="300" height="300"/>
      </div>
        <p class="text">El Mundo Aventura, parque temático que es tal vez el más popular de la ciudad, en él se pueden encontrar distintas áreas destinadas a cada edad, un ejemplo de ello es Mundo Pombo; un lugar destinado a los niños con atracciones para los más pequeños. Entre sus atracciones más famosas se encuentran su Zona para Paintball, el Skycoaster, el Extreme y los Troncos, atracción familiar donde las personas viajan en barcas y bajan de dos caídas de agua donde son totalmente salpicadas de agua.23​ Actualmente en el Parque Mundo Aventura se realiza el Evento Terror al Parque durante los fines de semana de octubre, en el que los turistas puede presenciar de espectáculos y visitar las estaciones de terror en medio de un ambiente de Halloween.
