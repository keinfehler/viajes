           <p class="texti">En la cima hay una variedad de cosas para ver y disfrutar. Monserrate ofrece una espectacular vista panorámica de la ciudad. El mirador junto al santuario es el lugar ideal para tomar fotografías de la ciudad y para apreciarla en toda su extensión. La Basílica del Señor Caído de Monserrate es una iglesia católica muy conocida, lugar tradicional de peregrinación de bogotanos y colombianos en general, es un buen lugar de reposo para aquellos cuya visita es de peregrinación.</p>
