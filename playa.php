      <h2 class="title">Actividades que no te puedes perder</h2>
              <p class="texti">Explora la diversidad del paisaje de Santa Marta y sus alrededores con la visita al Parque Nacional Natural Tayrona. En esta reserva natural podrás nadar en aguas cristalinas y caminar sobre la arena blanca. También, podrás disfrutar de sus amaneceres únicos ya que es considerada como una de las bahías más hermosas de América.</p>
              <p class="text">Otro destino obligado en los alrededores es la Sierra Nevada de Santa Marta, declarada Patrimonio de la Humanidad por la Unesco en 1979.  Es la montaña costera más alta del mundo con 5.775 metros sobre el nivel del mar. Como reserva de la Biosfera, habitada por cerca de 30 mil indígenas de las etnias Kogi, Arhuaco, Kankuaro y Wiwa. Este paisaje verde exuberante es ideal para caminar y realizar avistamiento de aves.</p>
              <p class="texti">Además del encanto natural, la oferta gastronómica es exquisita.  No olvides visitar los restaurantes en Santa Marta y disfrutar de la cocina samaria. El chipi chipi (un molusco), el pescado frito, los patacones, el arroz con coco y las cocadas son los imperdibles de la comida típica de Santa Marta.</p>

              <p class="text">Todo esto, sumado al encanto de su gente, su chispa y sabor Caribe, hacen que cualquier experiencia en Santa Marta, Colombia, sea inolvidable.</p>
              <p class="texti">A esa riqueza natural de la que también hace parte la Playa El Rodadero, hay que sumarle un impresionante legado histórico, representado en la arquitectura y en las calles del centro de Santa Marta, que ofrecen una ruta ideal para visitar la Catedral, los museos y el Malecón de Bastidas, donde además se puede gozar de un romántico atardecer.</p>
