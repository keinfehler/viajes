              <h2 class="infini">Cartagena</h2>

                <p class="text">Cartagena es una ciudad que está ubicada a orillas del Mar Caribe. Sus calles coloridas llenas de encanto la hacen la puerta de entrada a Suramérica. En Colombia, se encuentra al norte del país, y es la capital de la región de Bolívar. ‘La Heroica’, como la llaman, contempla a su alrededor varios archipiélagos e islas que son paraísos para un verdadero descanso.
                 Cartagena, suma a sus encantos los atractivos de una intensa vida nocturna, festivales culturales y paisajes exuberantes. Las playas de la ciudad te invitan a hacer turismo, descansar y divertirte con la refrescante brisa y las tibias aguas del mar.</p>
              <h2 class="info">¿Qué hacer?</h2>
        
                <p class="text">Indudablemente, debes conocer el patrimonio arquitectónico y cultural de la ciudad recorriendo su centro histórico, el cual se encuentra rodeado por largos kilómetros de antiguas murallas que conforman la ciudad amurallada. La belleza y la importancia de este paisaje de Cartagena hicieron que fuera declarada Patrimonio Histórico de la Humanidad por la Unesco en 1984.</p>

               <p class="text">En La Heroica, además de palpar cientos de años de historia en calles empedradas, podrás explorar el impresionante Castillo de San Felipe de Barajas, que data del siglo XVII. De esta manera, podrás conocer acerca de la historia de Cartagena y una descripción detallada de la ciudad.</p>

               <p class="text">Así mismo, podrás asombrarte con un recorrido por las iglesias antiguas y por monumentos famosos de Cartagena de Indias como los dedicados a la India Catalina, a los Zapatos Viejos y a los Pegasos. Otra de las actividades que puedes considerar si te estás preguntando qué hacer en Cartagena, es visitar el Museo Naval o el Centro Comercial La Serrezuela.</p>
               <p class="text">En estas caminatas, además de disfrutar de la agradable temperatura de Cartagena, podrás apreciar los ritmos locales que suenan en las calles y las artesanías que engalanan los comercios. Al caer la noche, el encanto de la zona colonial es perfecto para que recorras la ciudad sobre un carruaje guiado por caballos. Es una experiencia que te transportará en el tiempo.</p>
