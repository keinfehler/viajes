             <h2 class="infini">Medellín, La ‘Ciudad de la Eterna Primavera’</h2>
                <p style="font-size:15px; font-style:normal; text-align: left; color: black;">Medellín es una ciudad cautivadora, no solo por su clima agradable (ronda los 19 °C durante todo el año); también por la amabilidad de su gente, su deliciosa comida y ese encanto paisa que enamora a los visitantes.</p>

               <p class="text">El humor campesino de los habitantes de Medellín, que se refleja en las trovas antioqueñas, los bailes típicos y en general en la cultura paisa, se mezcla con la vida agitada de la metrópoli. No hay montañas ni ríos que puedan amilanar a estas personas, quienes tienen uno de los territorios más prolíficos del país, con grandes empresas y crecimiento en todos los sectores.</p>

               <p class="text">De hecho, el ingenio es una característica de los antioqueños, por lo que no es extraño que en 2013 Medellín haya sido nombrada la Ciudad más Innovadora del Mundo en el concurso City of the Year del Wall Street Journal.</p>
             <h2 class="info">¿Qué hacer en Medellín?</h2>
                <p class="text">El clima de Medellín, su paisaje montañoso, sus espectaculares vistas panorámicas y sus espacios verdes convierten los recorridos por la ciudad en una experiencia que seduce.</p>

                <p class="text">Lugares como el Jardín Botánico, el parque de los Pies Descalzos, el parque Berrío y el parque Lleras son sitios que no puedes dejar de visitar.</p>

                <p class="text">Lo mismo sucede con el Museo de Arte Moderno y el Museo de Antioquia, dedicado en muy buena parte a la obra del maestro colombiano Fernando Botero, cuyas esculturas engalanan muchas zonas de la ciudad.</p>

                <p class="text">Además, Medellín cuenta con el Parque Explora, un museo interactivo de ciencia y tecnología en el que niños y adultos pueden aprender y realizar una gran cantidad de actividades. Cerca de allí están el Planetario y el Parque de los Deseos.</p>

               <p class="text">En Medellín también abundan los centros comerciales, como El Tesoro o el Santafé, en los que podrás comprar todo tipo de artículos y disfrutar de una gran oferta gastronómica, pues otro gran plan es disfrutar de la comida típica de la región, que se compone de preparaciones como los buñuelos, el mondongo y la mítica bandeja paisa.</p>
 
