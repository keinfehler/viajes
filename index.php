             <h2 class="infini">San Andrés Isla</h2>
              <p class="text">Cerca de cuarenta sitios para bucear, unas de las mejores playas del Caribe, un mar azul pero que también es verde y de tonos lila, y gente llena de amabilidad… esos son algunos de los encantos de las islas de San Andrés, Providencia y Santa Catalina, ubicadas en el Caribe colombiano.</p>

              <p class="text">El Archipiélago, ubicado a unos 230 kilómetros al este de Centroamérica y a unos 750 kilómetros al norte del territorio continental colombiano, fue declarado en el año 2000 Reserva Mundial de la Biosfera “Seaflower”. Esta reserva no solo incluye las islas y sus cayos lejanos; además, conforma el 10% del mar Caribe, con una extensión mayor a los 300 mil kilómetros cuadrados.</p>

              <p class="text">A estas islas paradisíacas llegan viajeros de todo el mundo en busca de atardeceres perfectos, playas de arena blanca y altas palmeras que se mueven con la brisa y que, combinadas con las sonrisas despreocupadas de sus habitantes, convierten a este destino en uno de los mejores lugares para pasar unas vacaciones tropicales.</p>

              <p class="text">En este pequeño paraíso las influencias de ingleses, españoles, piratas y corsarios se mezclaron con el pueblo afro para dar como resultado una cultura de mucha riqueza. De hecho, seguramente escuches a los lugareños hablar creole, una particular mezcla de inglés isabelino, español y dialectos africanos.</p>

              <p class="text">¡Pero no te preocupes! Los sanandresanos también hablan español e inglés, así que la comunicación es muy sencilla.</p>


            <h2 class="info">¿Qué hacer en San Andrés?</h2>
              <p class="text">Desde el avión, la mejor tarjeta de presentación de San Andrés es su mar de siete colores. De hecho, el Archipiélago fue escogido como el mejor destino de Sol y Playa en los World Travel Awards de 2014, 2015 y 2019.

              <p class="text">Esto significa que si visitas Colombia no puedes perderte, bajo ninguna circunstancia, de la belleza natural de la Isla de San Andrés y de sus paradisíacas playas.</p>

              <p class="text">Entre las mejores playas de la isla se encuentra Spratt Bright, ubicada en el sector comercial de San Andrés. Esta bordeada por un malecón lleno de restaurantes, hoteles, bares y tiendas. Sus más de 2 kilómetros de extensión ofrecen diversas actividades desde simplemente tomar el sol, hasta disfrutar de deportes náuticos como el kitesurf y jetsky.</p>

              <p class="text">También está Sound Bay, una playa alejada del bullicio citadino que se encuentra en medio de la zona donde habitan los nativos de la isla.</p>

              <p class="text">Otra importante playa es Cocoplum, que está protegida por una barrera coralina cuyo mar calmado y aguas cristalinas la convierten en un sitio ideal para pasar un día tranquilo con los más pequeños de la familia. Además, la poca profundidad de sus aguas permite caminar hasta Rocky Cay, un cayo rocoso rodeado de vida submarina a unos 200 metros de la orilla.</p>
        
